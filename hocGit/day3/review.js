// day 2
// tìm hiểu về chức năng của các câu lệnh trong git 
// - đối với dự án cá nhân những câu lệnh cần thiết :
// + git init : giúp cho git quản lí các thay đổi file code trong dự án , tạo 1 file ẩn .git 
// + git add : thay đổi trạng thái các file code -> đưa các file code mới hoặc có thay đổi vào hàng chờ sẵn sàng
// + git status : kiểm tra trạng thái của các file code 
// + git commit -m : đưa phiên bản code hiện tại đang ở trạng thái sẵn sàng vào file .git -> lưu lại phiên bản code tại thời điểm đó , ai là người đẩy lên ,lời nhắn khi đẩy lên
// + git push : đẩy phiên bản code tại thời điểm đó lên trên kho lưu trữ cloud
// + git remote : kết nối với kho lưu trữ cloud để lưu phiên bản code đã được đẩy lên















